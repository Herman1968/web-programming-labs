// Task 2: Геометрична прогресія: a, a*r, a*r^2, ...
// ==================================================

let a = Number(prompt("Введіть перший елемент a:"));
let r = Number(prompt("Введіть співвідношення r:"));
let k = Number(prompt("Введіть кількість членів n:"));

console.log("=== Геометрична прогресія ===");

let sum = 0;
let element = a;

for (let i = 1; i <= k; i++) {
  console.log(element);
  sum += element;
  element *= r;
}

console.log("Сума прогресії = " + sum);

// ==================================================
// Task 3: Калькулятор у нескінченному циклі
// ==================================================

console.log("=== Калькулятор ===");
console.log("Для виходу введіть 'exit' замість операції.");

while (true) {
  let x = Number(prompt("Введіть перше число:"));
  let y = Number(prompt("Введіть друге число:"));
  let op = prompt("Введіть операцію (+, -, *, /, %) або 'exit' для виходу:");

  if (op === "exit") {
    alert("Вихід з програми.");
    break;
  }

  switch (op) {
    case "+":
      alert("Результат: " + (x + y));
      break;

    case "-":
      alert("Результат: " + (x - y));
      break;

    case "*":
      alert("Результат: " + x * y);
      break;

    case "/":
      if (y === 0) {
        alert("Помилка: ділення на нуль!");
      } else {
        alert("Результат: " + x / y);
      }
      break;

    case "%":
      alert("Результат: " + (x % y));
      break;

    default:
      alert("Невідома операція!");
  }
}
