// Task 1: Матриця з 1 та 0 розміру n*n
// =======================================

let n = Number(prompt("Введіть розмір матриці n:"));

console.log("=== Матриця з 1 і 0 ===");

for (let i = 0; i < n; i++) {
  let row = "";
  for (let j = 0; j < n; j++) {
    row += ((i + j) % 2) + " "; // чергування 1 і 0
  }
  console.log(row);
}

